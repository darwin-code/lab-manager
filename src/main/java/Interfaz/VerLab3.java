    private void pdfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pdfActionPerformed
        // Crear un documento
        Document documento = new Document();

        try {
            // Obtener la ruta del escritorio del usuario
            String ruta = System.getProperty("user.home") + "/Desktop/Reporte_Horario_Lab03.pdf";

            // Crear un escritor PDF
            PdfWriter.getInstance(documento, new FileOutputStream(ruta));
            documento.open();

            // Crear un objeto Paragraph para el título
            Paragraph titulo = new Paragraph("      UNIVERSIDAD DE LAS FUERZAS ARMADAS ESPE", FontFactory.getFont(FontFactory.HELVETICA_BOLD, 18));

            // Agregar el título al documento
            documento.add(titulo);
            documento.add(new Paragraph(" "));

            // Crear un objeto Paragraph para la información adicional
            Paragraph infoAdicional = new Paragraph("""
                Reporte de Horario
                A continuación se entregan las siguientes herramientas:
                * 30 computadores completos (CPU, monitor, teclado, mouse).
                * 30 cables de red, 30 sillas, 8 mesas trabajo estudiantes.
                * 1 proyector tiro corto.
                * 1 mesa de trabajo docente.""",
                FontFactory.getFont(FontFactory.HELVETICA, 12));

            // Agregar la información adicional al documento
            documento.add(infoAdicional);
            // Agregar un espacio en blanco
            documento.add(new Paragraph(" "));

            // Crear una tabla PDF con 7 columnas
            PdfPTable tabla = new PdfPTable(7);
            float[] columnWidths = {6, 20, 20, 15, 20, 18, 18};
            tabla.setWidths(columnWidths);

            // Tamaño de fuente para las celdas de la tabla
            com.itextpdf.text.Font fontCeldas = FontFactory.getFont(FontFactory.HELVETICA, 9);

            // Agregar encabezados de columna a la tabla
            tabla.addCell(new PdfPCell(new Phrase("ID", fontCeldas)));//1
            tabla.addCell(new PdfPCell(new Phrase("FECHA", fontCeldas)));//7
            tabla.addCell(new PdfPCell(new Phrase("HORA", fontCeldas)));//8
            tabla.addCell(new PdfPCell(new Phrase("DOCENTE", fontCeldas)));//2
            tabla.addCell(new PdfPCell(new Phrase("MATERIA", fontCeldas)));//3
            tabla.addCell(new PdfPCell(new Phrase("ALUMNOS", fontCeldas)));//4
            tabla.addCell(new PdfPCell(new Phrase("OBSERVACION", fontCeldas)));//5

            try {
                // Establecer la conexión con la base de datos
                Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/bdDatos", "root", "080210");

                // Preparar la consulta SQL
                PreparedStatement pst = cn.prepareStatement("SELECT * FROM usuarios3");

                // Ejecutar la consulta y obtener los resultados
                ResultSet rs = pst.executeQuery();

                // Iterar sobre los resultados y agregar filas a la tabla PDF
                while (rs.next()) {
                    tabla.addCell(new PdfPCell(new Phrase(rs.getString(1), fontCeldas)));
                    tabla.addCell(new PdfPCell(new Phrase(rs.getString(7), fontCeldas)));
                    tabla.addCell(new PdfPCell(new Phrase(rs.getString(8), fontCeldas)));
                    tabla.addCell(new PdfPCell(new Phrase(rs.getString(2), fontCeldas)));
                    tabla.addCell(new PdfPCell(new Phrase(rs.getString(3), fontCeldas)));
                    tabla.addCell(new PdfPCell(new Phrase(rs.getString(4), fontCeldas)));
                    tabla.addCell(new PdfPCell(new Phrase(rs.getString(5), fontCeldas)));

                }

                // Agregar la tabla al documento
                documento.add(tabla);

            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Error al ejecutar la consulta SQL");
            } finally {
                // Cerrar el documento al finalizar
                documento.close();
            }

            JOptionPane.showMessageDialog(null, "Reporte creado en: " + ruta);

        } catch (DocumentException | HeadlessException | FileNotFoundException e) {
            JOptionPane.showMessageDialog(null, "Error al crear el reporte PDF");
        }
    }//GEN-LAST:event_pdfActionPerformed

