    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed
// Obtener los valores de los campos de entrada
        String nombreText = nombre.getText();
        String materiaText = materia.getText();
        String alumnosText = alumnos.getText();
        String observacionText = observacion.getText();

        // Validar que todos los campos estÃ©n completos
        if (nombreText.isEmpty() || materiaText.isEmpty() || alumnosText.isEmpty() || observacionText.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor, complete todos los campos.");
            return; // Salir del mÃ©todo si algÃºn campo estÃ¡ vacÃ­o
        }

        // Verificar si la cantidad de alumnos ingresada es un nÃºmero vÃ¡lido
        try {
            int cantidadAlumnos = Integer.parseInt(alumnosText);
            if (cantidadAlumnos > 40) {
                // La cantidad de alumnos supera el lÃ­mite permitido, mostrar mensaje de advertencia y permitir que el usuario vuelva a ingresar la cantidad
                JOptionPane.showMessageDialog(null, "El laboratorio 04 permite capacidad mÃ¡xima de 40 alumnos. Ingrese nuevamente la cantidad de alumnos.");
                alumnos.setText(""); // Borrar el valor anterior de la cantidad de alumnos
                alumnos.requestFocus(); // Colocar el foco en el campo de cantidad de alumnos para que el usuario pueda ingresar el valor nuevamente
                return; // Salir del mÃ©todo temporalmente, esperando que el usuario ingrese un valor vÃ¡lido
            }
        } catch (NumberFormatException e) {
            // Se ingresÃ³ un valor no numÃ©rico en el campo de cantidad de alumnos, mostrar mensaje de error
            JOptionPane.showMessageDialog(null, "Por favor, ingrese un valor numÃ©rico vÃ¡lido en el campo de cantidad de alumnos.");
            alumnos.setText(""); // Borrar el valor ingresado
            alumnos.requestFocus(); // Colocar el foco en el campo de cantidad de alumnos para que el usuario pueda ingresar un valor vÃ¡lido
            return; // Salir del mÃ©todo temporalmente, esperando que el usuario ingrese un valor vÃ¡lido
        }

        // Si todos los campos estÃ¡n completos y la cantidad de alumnos es vÃ¡lida, proceder a guardar la informaciÃ³n
        Clases.Usuario4 objetoInformacion = new Clases.Usuario4();
        objetoInformacion.agregarInfo(dia, nombre, genero, materia, alumnos, observacion, horasCombo);

        // Limpiar los campos despuÃ©s de guardar la informaciÃ³n
        nombre.setText("");
        genero.setSelectedIndex(0);
        materia.setText("");
        alumnos.setText("");
        observacion.setText("");
        id.setText("");
    }//GEN-LAST:event_guardarActionPerformed

